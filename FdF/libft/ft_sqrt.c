int		ft_sqrt(int nb)
{
	int		i;
	int		r;

	r = 0;
	i = nb;
	if (nb == 1)
		return (1);
	if (nb <= 0 || nb > 2147395600)
		return (0);
	while (i != 0)
	{
		if ((r * r) != nb)
		{
			i--;
			r++;
		}
		else
			return (r);
	}
	return (0);
}
