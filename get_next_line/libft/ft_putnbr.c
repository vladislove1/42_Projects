#include "libft.h"

void	ft_putnbr(int n)
{
	int		nb;

	nb = n;
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n < 10 && nb != -2147483648)
		ft_putchar(n + '0');
	if (nb == -2147483648)
	{
		ft_putstr("2147483648");
	}
}
